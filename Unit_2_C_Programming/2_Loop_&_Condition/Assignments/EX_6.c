#include <stdio.h>

int main()
{
    int num, sum=0;
    while(1)
    {
        // Input a Number from user
        printf("Enter a Number :  ");

        scanf("%d",&num);

        for (int i =1 ; i <= num ; i++)
        {
            sum+=i;

        }
        printf("Sum = %d \n",sum);
        sum=0;
        printf("================================================\r\n");

    }
    return 0;
}
